package practica2;

public class WhileEjercicio2 {
	/*
	 * Pre: ---
	 * Post: esta clase Java implementa un método main que 
	 * muestra los números enteros entre 50 y 100 (ambos incluídos)
	 */
	public static void main(String[] args) {
		int i=50;
		while(i<=100) {
			System.out.println(i);
			i++;
		}
	}
}
