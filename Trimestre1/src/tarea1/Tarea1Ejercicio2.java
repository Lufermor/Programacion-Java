package tarea1;
/*
 * Implementa un programa Java que dados dos números enteros (x, y) te
muestre por pantalla la suma de ambos.
 */
public class Tarea1Ejercicio2 {
	public static void main(String[] args) {
		System.out.println(2+4);
		System.out.println("2+4");
		int x = 4;
		int y = 6;
		System.out.println(x + y);
		int suma = x + y;
		System.out.println("Esta es la suma: " + suma + " ok");
	}
}
